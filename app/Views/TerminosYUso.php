<h2>
<i class="bi bi-file-earmark-text" style = "color:white"></i>
Formas y procedimientos involucrados en la venta
</h2>
<ul style="list-style: none; padding: 0;">
  <li><strong>Formas de pago:</strong> transferencia bancaria, depósito, tarjeta de débito o financiación propia.</li>
  <li><strong>Entrega:</strong> el vehículo debe ser retirado personalmente en el local correspondiente.</li>
  <li><strong>Tiempo de retiro:</strong> hasta 30 días hábiles después de la confirmación del pago.</li>
  <li><strong>Garantía:</strong> los vehículos cuentan con garantía oficial de fábrica según la marca.</li>
  <li><strong>Soporte post venta:</strong> asistencia sobre cuestiones administrativas relacionadas con la adquisición.</li>
</ul>
