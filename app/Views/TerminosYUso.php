<h2>
<i class="bi bi-person-fill-lock" style = "color:white"></i>

Políticas de Privacidad</h2>

<ul style="list-style: none; padding: 0;">
    <li><strong>Qué datos personales se recopilan:</strong> Recopilamos información necesaria para procesar las operaciones de compra y brindar un mejor servicio, incluyendo nombre completo, dirección, correo electrónico, número de teléfono, datos de pago y, en algunos casos, número de documento de identidad.</li>
    <br>
    <li><strong>Cómo se utilizan los datos personales:</strong> Los datos personales proporcionados son utilizados para gestionar solicitudes, coordinar la compra y entrega de vehículos, brindar asesoramiento personalizado y enviar comunicaciones relevantes relacionadas con el servicio.</li>
    <br>
    <li><strong>Con quién se comparten los datos personales:</strong> Los datos no se comparten con terceros, salvo en los casos en que sea estrictamente necesario para completar la operación (por ejemplo, entidades financieras para gestión de financiación) o en cumplimiento de obligaciones legales. En ningún caso los datos son vendidos ni utilizados con fines publicitarios ajenos a Xport.</li>
    <br>
    <li><strong>Derechos de los usuarios sobre sus datos personales:</strong> El usuario tiene derecho a acceder, actualizar, rectificar y eliminar sus datos personales en cualquier momento. Para ejercer estos derechos, puede comunicarse directamente con nosotros a través de nuestros canales de contacto oficiales.</li>
    <br>
    <li><strong>Medidas de seguridad para proteger los datos personales:</strong> Implementamos medidas técnicas y organizativas adecuadas para garantizar la seguridad de los datos personales, previniendo accesos no autorizados, pérdidas o alteraciones. Nuestro sistema cumple con los estándares de protección de datos vigentes.</li>
</ul>

